class Solution:
    def sampleStats(self, count) :

        minimum, maximum, mean, median, mode=float('inf'),float('-inf'),0,0,0
        suma,n=0,0
        for i,v in enumerate(count):
            if v!=0:
                minimum=min(minimum,i)
                maximum=max(maximum,i)
                if count[mode]<v:
                    mode=i
                suma+=i*v
                n+=v
        mean=suma/n

        index=0
        a1,a2=0,0
        for i,v in enumerate(count):
            if v==0:
                continue
            if index<n//2 and index+v>=n//2:
                a1=i
            if index<n//2+1 and index+v>=n//2+1:
                a2=i
            index+=v
        if n%2==0:
            median=(a1+a2)/2
        else:
            median=a2
        return minimum, maximum, mean, median, mode

